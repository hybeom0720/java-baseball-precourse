package baseball;

public class GamePoint {
    private int ball;
    private int strike;

    public void setBall(int ballPoint) {
        ball = ballPoint;
    }

    public void setStrike(int strikePoint) {
        strike = strikePoint;
    }

    public int getBall() {
        return ball;
    }

    public int getStrike() {
        return strike;
    }
}
